                <dl>
                    <dt id="A">Air Conditioning</dt>
                    <dd>A system used for cooling and sometimes dehumidifying the air in a building. Air conditioners work by removing heat from indoor air and transferring it outside, creating a more comfortable indoor environment.</dd>
                    
                    <dt>Air Handler</dt>
                    <dd>A unit within an HVAC system that circulates and regulates air. Typically, it consists of a blower, heating or cooling elements, filter racks, and dampers. Air handlers connect to ductwork that distributes the conditioned air throughout the building and returns it to the AHU.</dd>
                    
                    <dt>Airflow</dt>
                    <dd>The movement of air through a space, which is crucial in HVAC systems for maintaining indoor air quality and comfort. Proper airflow ensures efficient operation of the system and distributes conditioned air evenly.</dd>
                    
                    <dt>Air Quality</dt>
                    <dd>Refers to the cleanliness and healthiness of the air inside a building. Good air quality is essential for comfort and health, and HVAC systems play a vital role in filtering and purifying indoor air to remove pollutants and allergens.</dd>
                    
                    <dt>AFUE &#40;Annual Fuel Utilization Efficiency&#41;</dt>
                    <dd>A measure of a gas furnace&apos;s efficiency in converting fuel to energy. The higher the AFUE percentage, the more efficient the furnace. For example, an AFUE of 90% means that 90% of the fuel is used to heat the home, and the rest is lost in the combustion process.</dd>
                    
                    <dt>Accumulator</dt>
                    <dd>A component in an HVAC system designed to protect the compressor from liquid refrigerant. It&apos;s particularly important in heat pump systems, as it prevents liquid refrigerant from entering the compressor, which can cause damage.</dd>
                    
                    <dt>ACCA &#40;Air Conditioning Contractors of America&#41;</dt>
                    <dd>A non&dash;profit association in the United States that represents HVAC contractors. They work to promote professional contracting, energy efficiency, and healthy, comfortable indoor environments.</dd>
                    
                    <dt>Actuator</dt>
                    <dd>A device in an HVAC system used to control the opening and closing of dampers in ductwork, regulating airflow. Actuators can be either manual or motorized and are essential for zone control in HVAC systems.</dd>
                    
                    <dt>ASHRAE &#40;American Society of Heating, Refrigerating and Air&dash;Conditioning Engineers&#41;</dt>
                    <dd>An international technical society dedicated to advancing the arts and sciences of heating, ventilation, air conditioning, and refrigeration to serve humanity and promote a sustainable world.</dd>
                    
                    <dt>Ambient Temperature</dt>
                    <dd>The temperature of the surrounding air. In HVAC terms, it often refers to the outdoor air temperature, which can impact the performance of air conditioning and heating systems.</dd>
                    
                    <dt>Attenuator</dt>
                    <dd>A device used in HVAC systems to reduce noise in the ductwork. It is designed to absorb sound and is often used in situations where duct noise could be disruptive.</dd>
                    
                    <dt>Air Filter</dt>
                    <dd>A component in HVAC systems that removes particles, such as dust, pollen, and bacteria, from the air, improving indoor air quality. Air filters require regular maintenance and replacement to ensure efficiency and cleanliness.</dd>
                    
                    <dt id="B">BTU &#40;British Thermal Unit&#41;</dt>
                    <dd>A unit of measure for heat. It represents the amount of thermal energy required to raise the temperature of one pound of water by one degree Fahrenheit. In HVAC, BTU is used to measure the heating and cooling capacity of equipment.</dd>
                    
                    <dt>Boiler</dt>
                    <dd>A heating system that heats water or generates steam to provide heating through radiators or a hydronic heating system. Boilers are commonly used in residential and commercial buildings for efficient heating solutions.</dd>
                    
                    <dt>Blower Motor</dt>
                    <dd>A component in HVAC systems that drives the fan or blower, circulating air through the ducts and into the space. Blower motors can be single&dash;speed, multi&dash;speed, or variable&dash;speed, affecting the efficiency and control of airflow.</dd>

                    <dt>Bypass Damper</dt>
                    <dd>A device in ductwork that allows air to bypass a section of the system. It&apos;s often used in zoned HVAC systems to maintain proper air pressure and ensure consistent temperatures in different areas of a building.</dd>

                    <dt>Balancing &#40;Air/Water&#41;</dt>
                    <dd>The process of adjusting an HVAC system to distribute the correct amount of air or water flow to different areas in a building, ensuring optimal performance and comfort.</dd>

                    <dt>Building Automation System &#40;BAS&#41;</dt>
                    <dd>A system that automates the control of various building systems, including HVAC, lighting, and security. BAS can enhance energy efficiency, reduce operating costs, and improve occupant comfort.</dd>

                    <dt>Backdraft Damper</dt>
                    <dd>A damper installed in the ductwork that prevents outside air from flowing back into the system. It&apos;s particularly important in exhaust systems to prevent contaminated air from re&dash;entering the building.</dd>

                    <dt>Barometric Relief</dt>
                    <dd>A method or device used in HVAC systems to maintain a specific air pressure within a space. It works by automatically adjusting to allow excess air to escape, thereby balancing the pressure.</dd>

                    <dt>Brazing</dt>
                    <dd>A process used in HVAC for joining metal parts &#40;typically copper&#41; by melting and flowing a filler metal into the joint. Brazing is stronger than soldering and is commonly used in piping and refrigeration systems.</dd>

                    <dt>British Standard Pipe &#40;BSP&#41;</dt>
                    <dd>A standard for screw threads commonly used in the plumbing and pipe fitting industry, including HVAC systems. It&apos;s crucial for ensuring proper fitting and sealing in piping connections.</dd>

                    <dt>Bimetal</dt>
                    <dd>A temperature&dash;sensing element made of two dissimilar metals joined together. It&apos;s used in some thermostats and controls in HVAC systems to sense temperature changes and activate switches or other controls.</dd>

                    <dt>Buffer Tank</dt>
                    <dd>A tank used in HVAC systems, particularly in hydronic heating or cooling systems, to store hot or chilled water. The buffer tank helps to stabilize temperature fluctuations and improve system efficiency.</dd>

                    <dt>BACnet &#40;Building Automation and Control Networks&#41;</dt>
                    <dd>A communication protocol for building automation and control systems, including HVAC. It allows different systems and devices to communicate with each other, enhancing integration and control.</dd>

                    <dt id="C">Condenser</dt>
                    <dd>A component in air conditioning and refrigeration systems that condenses a substance from its gaseous to its liquid state, thereby releasing heat. In HVAC systems, the condenser is typically part of the outdoor unit.</dd>

                    <dt>Compressor</dt>
                    <dd>A key part of an air conditioning or refrigeration system, the compressor increases the pressure of refrigerant gas and moves it through the system. It&apos;s often considered the heart of the system.</dd>

                    <dt>CFM &#40;Cubic Feet per Minute&#41;</dt>
                    <dd>A measure of airflow volume, indicating how many cubic feet of air pass by a stationary point in one minute. In HVAC, CFM is used to gauge the effectiveness of the air distribution system.</dd>

                    <dt>Chiller</dt>
                    <dd>A machine that removes heat from a liquid via a vapor&dash;compression or absorption refrigeration cycle. This chilled liquid can then be circulated through a heat exchanger to cool equipment or another process stream.</dd>

                    <dt>Condensate</dt>
                    <dd>The liquid that forms when air is cooled below its dew point and moisture condenses. In HVAC systems, condensate is often seen as water dripping from an air conditioner&apos;s indoor unit or drain line.</dd>

                    <dt>Carbon Monoxide &#40;CO&#41;</dt>
                    <dd>A colorless, odorless gas that can be harmful when inhaled in large amounts. It&apos;s important for HVAC systems, especially those that burn fossil fuels, to have proper ventilation to prevent CO buildup.</dd>

                    <dt>Coil</dt>
                    <dd>Refers to the serpentine loops, usually made of copper or aluminum, through which refrigerant flows. In HVAC systems, there are typically two types of coils&colon; evaporator coils &#40;which absorb heat&#41; and condenser coils &#40;which release heat&#41;.</dd>

                    <dt>COP &#40;Coefficient of Performance&#41;</dt>
                    <dd>A measure of efficiency for a heating or cooling system. It represents the ratio of useful heating or cooling provided to the energy consumed. Higher COP values indicate a more efficient system.</dd>

                    <dt>Compressor Overload Protector</dt>
                    <dd>A safety device that shuts off the compressor if it becomes dangerously hot or draws too much power, helping to prevent damage and potential failures.</dd>

                    <dt>Central Air Conditioning</dt>
                    <dd>A system that cools and dehumidifies air in multiple rooms or an entire building through one central location. It typically includes a compressor, condenser, and evaporator, and uses ducts to distribute the conditioned air.</dd>

                    <dt>Capacitor</dt>
                    <dd>An electrical component that stores and releases electrical energy. In HVAC systems, capacitors are used with motors, like those in the compressor and blower motor, to provide a starting boost and stabilize running power.</dd>

                    <dt>Condensate Pump</dt>
                    <dd>A pump used to remove water &#40;condensate&#41; that&apos;s been collected in a catch pan, typically found in central air conditioning systems, furnaces, and dehumidifiers.</dd>

                    <dt>CFCs &#40;Chlorofluorocarbons&#41;</dt>
                    <dd>Chemical compounds once commonly used as refrigerants in air conditioning systems. Due to their harmful effect on the ozone layer, CFCs have been largely phased out and replaced with more environmentally friendly substances.</dd>

                    <dt id="D">Ductwork</dt>
                    <dd>The system of ducts used to transport air from the HVAC unit throughout the building. Properly designed and maintained ductwork is crucial for efficient air distribution and indoor comfort.</dd>

                    <dt>Damper</dt>
                    <dd>A device installed in ductwork that regulates airflow by opening or closing. Dampers are used to balance airflow and control the temperature in different zones of a building.</dd>

                    <dt>Diffuser</dt>
                    <dd>An outlet device mounted on the ceiling or wall that spreads the conditioned air in various directions. Diffusers are designed to mix the conditioned air with the existing air in the room effectively.</dd>

                    <dt>Dehumidifier</dt>
                    <dd>A device that removes excess moisture from the air. In HVAC systems, dehumidifiers help to maintain comfortable humidity levels and prevent problems like mold growth and dampness.</dd>

                    <dt>Dry Bulb Temperature</dt>
                    <dd>A measure of air temperature independent of humidity. It&apos;s called &apos;dry bulb&apos; because the air temperature is measured without the influence of moisture, as opposed to wet bulb temperature which considers humidity.</dd>

                    <dt>Downflow Furnace</dt>
                    <dd>A type of furnace in which air enters the top of the unit and is heated as it moves downward. This design is often used in homes with limited space or in basements where the furnace must fit under the floor.</dd>

                    <dt>Ductless System</dt>
                    <dd>An HVAC system that provides heating or cooling without the need for ductwork. Commonly known as mini&dash;split systems, they are often used in additions to homes, small apartments, or rooms where installing ductwork is impractical.</dd>

                    <dt>Drain Pan</dt>
                    <dd>Also known as a condensate pan, this is a tray placed under the evaporator coil to catch condensation as it drips off the coil, preventing water damage and leaks.</dd>

                    <dt>Dew Point</dt>
                    <dd>The temperature at which air becomes saturated with moisture and dew can form. Understanding dew point is important in HVAC for controlling humidity and preventing condensation problems.</dd>

                    <dt>Duct Sealing</dt>
                    <dd>The process of sealing joints, seams, and gaps in ductwork to prevent air leaks. Proper duct sealing improves the efficiency of the HVAC system and reduces energy costs.</dd>

                    <dt>Delta T (Î”T)</dt>
                    <dd>Stands for &apos;temperature difference.&apos; In HVAC, it often refers to the difference in temperature between the air entering and the air exiting a system component, like a coil, indicating the effectiveness of heat transfer.</dd>

                    <dt>Duct Insulation</dt>
                    <dd>Insulation material wrapped around ductwork to prevent heat loss or gain and condensation. Insulating ducts is important for maintaining energy efficiency in HVAC systems.</dd>

                    <dt>Demand Response</dt>
                    <dd>A program in which consumers reduce or shift their electricity use during peak periods in response to time-based rates or other forms of financial incentives. This concept is increasingly being integrated into smart HVAC systems for energy savings.</dd>

                    <dt id="E">Evaporator Coil</dt>
                    <dd>A part of an HVAC system where the refrigerant absorbs heat from the air, causing it to evaporate. In air conditioning systems, this component is typically located inside or near the air handler where it cools the indoor air.</dd>

                    <dt>Energy Efficiency Ratio &#40;EER&#41;</dt>
                    <dd>A measure of how efficiently an air conditioning system will operate when the outdoor temperature is at a specific level. It&apos;s calculated by dividing the cooling capacity &#40;in BTUs per hour&#41; by the power input &#40;in watts&#41;.</dd>

                    <dt>Exhaust Ventilation</dt>
                    <dd>The process of removing air from inside a building to the outside. This is crucial for eliminating pollutants, moisture, and odors, and for maintaining indoor air quality.</dd>

                    <dt>Expansion Valve</dt>
                    <dd>A device in an HVAC system that controls the amount of refrigerant released into the evaporator, thereby controlling the cooling process.</dd>

                    <dt>Economizer</dt>
                    <dd>A system that reduces energy consumption by using outside air to cool a building. When the outdoor air is cooler than the indoor air, the economizer brings in the cooler air, reducing the need for artificial cooling.</dd>

                    <dt>Energy Recovery Ventilator &#40;ERV&#41;</dt>
                    <dd>A device that exchanges stale indoor air with fresh outdoor air, while also transferring heat and moisture between the incoming and outgoing air streams, thus saving energy.</dd>

                    <dt>Electronic Air Cleaner &#40;EAC&#41;</dt>
                    <dd>A device that uses electrical charges to filter and remove particles from the air, like dust, pollen, and smoke. It&apos;s more effective than standard air filters and is used in HVAC systems to improve indoor air quality.</dd>

                    <dt>Evaporative Cooler</dt>
                    <dd>Also known as a swamp cooler, this device cools air through the evaporation of water, which is an energy&dash;efficient alternative to traditional air conditioning in certain climates.</dd>

                    <dt>End Cap &#40;in ductwork&#41;</dt>
                    <dd>A cap or cover used to seal the end of a duct, preventing air leakage and ensuring that air is directed to the intended locations.</dd>

                    <dt>Entalpy</dt>
                    <dd>A term used in thermodynamics to describe the total heat content of air. In HVAC, it&apos;s important for calculating the energy efficiency of heating and cooling processes.</dd>

                    <dt>Electric Resistance Heating</dt>
                    <dd>A type of heating system where electric current is used to heat a resistant element, which then radiates heat to the surrounding area. It&apos;s simple but often less energy-efficient than other heating methods.</dd>

                    <dt>Energy Star</dt>
                    <dd>A program run by the U.S. Environmental Protection Agency &#40;EPA&#41; that certifies buildings and products for energy efficiency. HVAC systems with the Energy Star label meet strict energy efficiency guidelines set by the EPA.</dd>

                    <dt>EER &#40;Energy Efficiency Ratio&#41;</dt>
                    <dd>A measure of how efficiently an air conditioning unit uses electricity. It&apos;s calculated by dividing the cooling capacity by the power input. Higher EER ratings indicate more efficient equipment.</dd>

                    <dt id="F">Furnace</dt>
                    <dd>A heating system that warms air which is then distributed throughout a building via ductwork. Furnaces can be powered by various fuel sources, including natural gas, propane, oil, or electricity.</dd>

                    <dt>Filter Drier</dt>
                    <dd>A device in HVAC systems that removes moisture and contaminants from the refrigerant. This helps to prevent blockages and maintain the efficiency and longevity of the system.</dd>

                    <dt>Freon</dt>
                    <dd>A brand name for a type of refrigerant used in air conditioning systems. It&apos;s important to note that some Freon types have been phased out due to environmental concerns.</dd>

                    <dt>Fan Coil Unit &#40;FCU&#41;</dt>
                    <dd>A simple device consisting of a heating or cooling coil and fan. It&apos;s part of an HVAC system and is typically used to control the temperature in a single room or zone.</dd>

                    <dt>Flow Rate</dt>
                    <dd>In HVAC, this refers to the volume of air, water, or refrigerant moving through the system. Proper flow rate is crucial for the efficiency and effectiveness of HVAC systems.</dd>

                    <dt>Flue</dt>
                    <dd>A duct or opening that conveys exhaust gases from a furnace, water heater, boiler, or fireplace to the outdoors. Proper flue design is essential for safety and efficiency.</dd>

                    <dt>Fresh Air Intake</dt>
                    <dd>A feature in HVAC systems that brings in fresh outdoor air to replace indoor air that has become stale or polluted. This is important for maintaining good indoor air quality.</dd>

                    <dt>Fan Limit Switch</dt>
                    <dd>A control device in furnaces that turns the fan on and off based on the temperature within the furnace. It helps to prevent the system from overheating and ensures efficient operation.</dd>

                    <dt>Fancoil</dt>
                    <dd>An HVAC component that consists of a coil and a fan, used to either heat or cool the air. Fancoils are often used in split system air conditioners.</dd>

                    <dt>Fire Damper</dt>
                    <dd>A safety device installed in ductwork that closes automatically in the event of a fire to prevent the spread of flames and smoke through the ducts.</dd>

                    <dt>Foot Candle</dt>
                    <dd>A measure of light intensity, indicating how much light is received from a source one foot away. While not directly an HVAC term, it&apos;s relevant in building design and comfort levels.</dd>

                    <dt>Fahrenheit</dt>
                    <dd>A temperature scale commonly used in the United States to measure air temperature in HVAC systems. The freezing point of water is 32 degrees Fahrenheit and the boiling point is 212 degrees Fahrenheit.</dd>

                    <dt>Flex Duct</dt>
                    <dd>A type of lightweight, flexible ductwork used in HVAC systems. It&apos;s easier to install than rigid ductwork and is commonly used in residential applications.</dd>

                    <dt id="G">Grille</dt>
