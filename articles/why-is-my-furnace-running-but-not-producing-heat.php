        <section class="blog-section blog-width-limiter">
            <h2 id="thermostat-issues">1. Thermostat Issues</h2>
            <p>The thermostat is the brain of your heating system. If it's not working correctly, your furnace won't know when or how to heat your home.</p>
            <img src="../blogimages/guide-to-ac-thermostat.webp" alt="Closeup of hands adjusting a thermostat" width="800" height="274">
            <h3>Incorrect Settings</h3>
            <p>Ensure your thermostat is set to "Heat" mode and the temperature is set higher than the current room temperature. It might sound simple, but accidental adjustments can happen.</p>
            <h3>Calibration Problems</h3>
            <p>If your thermostat isn't accurately reading the temperature, it may not signal the furnace to produce heat. Consider recalibrating it or replacing the batteries if it's a digital model.</p>
            <h3>Loose Wiring or Connection Issues</h3>
            <p>Wiring problems can prevent the thermostat from communicating with the furnace. If you suspect this, it's best to consult a professional technician.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="clogged-air-filter">2. Clogged Air Filter</h2>
            <p>A dirty or clogged air filter restricts airflow, which can cause the furnace to overheat and shut down the heating cycle.</p>
            <img src="../blogimages/changing-filter.webp" alt="A homeowner removes an air filter from a furnace" width="800" height="430">
            <h3>Impact of a Dirty Filter</h3>
            <p>When the filter is clogged, the furnace struggles to pull in enough air. This can trigger safety mechanisms that prevent the furnace from heating to avoid overheating.</p>
            <h3>Solution</h3>
            <p>Check your air filter and replace it if it's dirty. Regularly changing your filter every 1-3 months can prevent this issue and improve indoor air quality.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="pilot-light-outage">3. Pilot Light or Ignition Problems</h2>
            <p>For gas furnaces, the pilot light or electronic ignition is essential for starting the heating process.</p>
            <img src="../blogimages/furnace-guide-furnaces.webp" alt="Gas furnaces displayed side by side" width="1200" height="625">
            <h3>Pilot Light Is Out</h3>
            <p>If the pilot light has gone out, the furnace can't ignite the burners to produce heat.</p>
            <h3>Faulty Ignition System</h3>
            <p>Modern furnaces use electronic ignition systems, which can fail due to component wear or electrical issues.</p>
            <h3>What to Do</h3>
            <p>For a pilot light outage, you can try relighting it following the manufacturer's instructions. If you're uncomfortable doing this or have an electronic ignition, contact a professional technician.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="gas-supply">4. Gas Supply Issues</h2>
            <p>Your furnace requires a steady gas supply to function. Any disruption can prevent it from producing heat.</p>
            <img src="../blogimages/furnace-fuel-sources.webp" alt="Infographic of gas, electric, and oil furnaces" width="800" height="878">
            <h3>Closed Gas Valve</h3>
            <p>Sometimes, the gas valve leading to the furnace may be accidentally turned off.</p>
            <h3>Gas Line Problems</h3>
            <p>Issues with the gas line, such as leaks or pressure problems, can disrupt the gas flow.</p>
            <h3>Action Steps</h3>
            <p>Check that the gas valve is open. If you suspect a gas leak or other gas line issues, evacuate your home immediately and call your gas company and a professional HVAC technician.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="faulty-blower-motor">5. Faulty Blower Motor</h2>
            <p>The blower motor is responsible for circulating air throughout your home. If it's running without producing heat, there may be an issue.</p>
            <img src="../blogimages/heat-distribution.webp" alt="Illustration of heat distribution in a home" width="1200" height="625">
            <h3>Signs of a Blower Motor Issue</h3>
            <div class="in-article-list">
                <ul class="in-article-list">
                    <li><span>Unusual Noises</span> &dash; Grinding or squealing sounds indicate mechanical problems.</li>
                    <li><span>Weak Airflow</span> &dash; Reduced airflow can mean the motor is failing.</li>
                    <li><span>Continuous Running</span> &dash; If the blower runs nonstop without heat, it's a red flag.</li>
                </ul>
            </div>
            <h3>Solution</h3>
            <p>A faulty blower motor often requires professional repair or replacement. Contact Bill's Heating & A/C for expert assistance.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="ductwork-problems">6. Ductwork Issues</h2>
            <p>Problems with your home's ductwork can prevent warm air from reaching your living spaces.</p>
            <img src="../blogimages/ductwork-inspection.webp" alt="Technician inspecting ductwork for leaks" width="800" height="533">
            <h3>Leaks and Disconnections</h3>
            <p>Leaks, holes, or disconnected ducts allow warm air to escape before reaching the vents.</p>
            <h3>Blocked Vents</h3>
            <p>Obstructions in the ducts or vents, such as debris or closed dampers, can impede airflow.</p>
            <h3>Resolution</h3>
            <p>Inspect visible ductwork for issues. Professional duct inspection and sealing can improve airflow and heating efficiency.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="safety-switches">7. Tripped Safety Switches</h2>
            <p>Furnaces come equipped with safety switches to prevent dangerous operating conditions.</p>
            <img src="../blogimages/thermostat-guide-programmable.webp" alt="Man adjusting a programmable thermostat" width="800" height="417">
            <h3>Limit Switch</h3>
            <p>The limit switch shuts off the furnace burners if it detects overheating, but the blower may continue to run to cool the system.</p>
            <h3>Rollout Switch</h3>
            <p>This switch detects flames or heat outside the combustion chamber and will shut down the furnace for safety.</p>
            <h3>Next Steps</h3>
            <p>If a safety switch has tripped, it's essential to identify and fix the underlying issue. This typically requires a professional technician's expertise.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="furnace-age">8. Age of the Furnace</h2>
            <p>An older furnace may struggle to heat effectively due to worn-out components or outdated technology.</p>
            <img src="../blogimages/old-furnace.webp" alt="An old furnace in a basement" width="800" height="533">
            <h3>Signs of an Aging Furnace</h3>
            <div class="in-article-list">
                <ul class="in-article-list">
                    <li><span>Frequent Repairs</span> &dash; Constant breakdowns indicate the furnace is nearing the end of its lifespan.</li>
                    <li><span>Inefficiency</span> &dash; Higher energy bills without increased usage can signal inefficiency.</li>
                    <li><span>Uneven Heating</span> &dash; Inconsistent temperatures throughout your home.</li>
                </ul>
            </div>
            <h3>Consider Replacement</h3>
            <p>If your furnace is over 15-20 years old, it might be time to consider upgrading to a new, energy-efficient model. This can improve comfort and reduce energy costs.</p>
        </section>

        <section class="blog-section blog-width-limiter">
            <h2 id="when-to-call-professional">9. When to Call a Professional</h2>
            <p>While some minor issues can be addressed by homeowners, many furnace problems require professional attention.</p>
            <img src="../blogimages/smiling-tech-large.webp" alt="HVAC technician performing maintenance on a furnace" width="800" height="533">
            <h3>Indicators You Need Expert Help</h3>
            <div class="in-article-list">
                <ul class="in-article-list">
                    <li><span>Persistent Issues</span> &dash; Problems that continue after basic troubleshooting.</li>
                    <li><span>Strange Noises or Smells</span> &dash; Unusual sounds or odors can indicate serious issues.</li>
                    <li><span>System Won't Turn On</span> &dash; If the furnace doesn't start at all.</li>
                    <li><span>Gas Odors</span> &dash; Evacuate immediately and call professionals if you smell gas.</li>
                </ul>
            </div>
